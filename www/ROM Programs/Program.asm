;Тестовая программа на ассемблере 8-bit AON Computer

start:
	MOV AX,01h   ;001000 00 0000 0001   ;20 01
	MOV BX,02h   ;001000 01 0000 0010   ;21 02
	ADD AX,BX    ;1 000 01 00           ;84

	MOV CX,E0h   ;001000 10 0000 0111   ;22 07
	ST [CX],AX   ;0001 10 00            ;18
	LD DX,[CX]   ;0000 10 11            ;0B
	
	ADD CX,BX    ;1 000 01 10           ;86
	MOV DX,21h   ;001000 11 0010 1011   ;23 21
	ST [CX],DX   ;0001 10 11            ;1B
end start